      SUBROUTINE MC34F (XL,YL,SIDE,NITER,MR,MI,COL)
C
         IMPLICIT REAL*8 (A-H,O-Z)
C
         REAL*8 XL,YL,SIDE,AR,AI,MR,MI
C
      INTEGER*4   COL(0:400,0:400)
      COMPLEX*16  A,B,C,Z,V,P,PP
      COMPLEX*16  Z0,Z1,Z2,Z3,Z4,Z5,Z6,Z7,Z8,Z9,Z10,Z11,ZLAST
      COMPLEX*16  Z0P,Z1P,Z2P,Z3P,Z4P,Z5P,Z6P,Z7P,Z8P,Z9P,Z10P,Z11P
C
C***********************************************************************
C***********************************************************************
C
      CALL NOERR
C
      EPS    =  1.E-6
C
C***********************************************************************
C***********************************************************************
C
      DO 2000 IY = 0,400
C
         Y = YL + (IY*SIDE)/400
      WRITE (6,*) IY
C
         DO 1000 IX = 0,400
C
            X = XL + (IX*SIDE)/400
C
C***********************************************************************
C***********************************************************************
C
      A = DCMPLX(X,Y)
C
      ZLAST = 1E10
C
      Z0 = DCMPLX(MR,MI)
C
      DO 400 I = 1,NITER
C
C         IF (ABS(REAL(Z0)) .GT. 50000D00   .OR.
C     *       ABS(IMAG(Z0)) .GT. 50000D00)  GOTO 420
         Z1 = Z0*Z0*Z0 + (A - 1)*Z0 - A
         Z2 = Z1*Z1*Z1 + (A - 1)*Z1 - A
         Z3 = Z2*Z2*Z2 + (A - 1)*Z2 - A
C
C        Z0P = 1
         Z1P = 3*Z0*Z0     + (A - 1)
         Z2P = 3*Z1*Z1*Z1P + (A - 1)*Z1P
         Z3P = 3*Z2*Z2*Z2P + (A - 1)*Z2P
C
         Z0 = Z0 - (Z3 - Z0)/(Z3P - 1)
C
         V = Z0 - ZLAST
         IF (REAL(V)*REAL(V) + IMAG(V)*IMAG(V) .LE. EPS) GOTO 800
C
         ZLAST = Z0
C
400   CONTINUE
C
      COL(IX,IY) = 0
      GOTO 1000
C
420   COL(IX,IY) = -1
      GOTO 1000
C
800   COL(IX,IY) = I
      GOTO 1000
C
C***********************************************************************
C***********************************************************************
C
1000  CONTINUE

2000  CONTINUE
C
C***********************************************************************
C***********************************************************************
C
      RETURN
      END
